package org.webonise.lambdafundamentals;

public class Apple {
    private final int weight;
    private final String color;

    Apple(final int weight, final String color) {
        this.weight = weight;
        this.color = color;
    }

    public int getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }
}


